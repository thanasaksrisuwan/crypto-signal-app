                for ws in disconnected_websockets:
                    if ws in self.active_connections:
                        self.disconnect(ws)
                
                if self.active_connections and not disconnected_websockets:
                    print(f"üíì ‡∏™‡πà‡∏á heartbeat ‡πÑ‡∏õ‡∏¢‡∏±‡∏á {len(self.active_connections)} connections")
                
