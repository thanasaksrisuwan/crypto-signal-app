    async def send_heartbeats(self):
        """‡∏™‡πà‡∏á heartbeat ‡πÑ‡∏õ‡∏¢‡∏±‡∏á clients ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏±‡∏Å‡∏©‡∏≤‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠"""
