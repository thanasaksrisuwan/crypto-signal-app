/**
 * คำอธิบายตัวช่วยสำหรับตัวบ่งชี้ทางเทคนิค
 * ไฟล์นี้รวบรวมคำอธิบายและเกร็ดความรู้เกี่ยวกับตัวบ่งชี้ทางเทคนิคต่างๆที่ใช้ในแอป
 */

/**
 * คำแนะนำการใช้ตัวบ่งชี้ทางเทคนิคเพื่อการเทรดอย่างมีประสิทธิภาพ
 * 
 * 1. การใช้ RSI (Relative Strength Index)
 * ------------------------------
 * เมื่อเปิดใช้งาน RSI คุณจะเห็นเส้นกราฟที่เคลื่อนไหวระหว่าง 0-100
 * 
 * วิธีการอ่านค่า:
 * - RSI > 70: สินทรัพย์อาจอยู่ในภาวะซื้อมากเกินไป (Overbought) เป็นสัญญาณขาย
 * - RSI < 30: สินทรัพย์อาจอยู่ในภาวะขายมากเกินไป (Oversold) เป็นสัญญาณซื้อ
 * - การทะลุเส้น 50 จากด้านล่างขึ้นบน: อาจเป็นสัญญาณซื้อ
 * - การทะลุเส้น 50 จากด้านบนลงล่าง: อาจเป็นสัญญาณขาย
 * 
 * เทคนิคการใช้งาน:
 * - ใช้ RSI ร่วมกับเส้นแนวโน้ม: เมื่อราคาทำจุดสูงสุดใหม่แต่ RSI ไม่ได้ทำจุดสูงสุดใหม่ (Bearish Divergence)
 * - ใช้ระดับ Oversold ในตลาดขาขึ้น และใช้ระดับ Overbought ในตลาดขาลง
 * - ปรับค่า Default Period (14) ให้เหมาะกับสไตล์การเทรด: ค่าต่ำลง = อ่อนไหวมากขึ้น
 * 
 * 
 * 2. การใช้ MACD (Moving Average Convergence Divergence)
 * --------------------------------------------
 * เมื่อเปิดใช้งาน MACD คุณจะเห็นเส้น 2 เส้นและแท่งกราฟ (Histogram)
 * 
 * วิธีการอ่านค่า:
 * - เส้น MACD (สีน้ำเงิน) = EMA เร็ว (12) - EMA ช้า (26)
 * - เส้นสัญญาณ (สีส้ม) = EMA (9) ของเส้น MACD 
 * - แท่งกราฟแสดงความแตกต่างระหว่างเส้น MACD และเส้นสัญญาณ
 * 
 * เทคนิคการใช้งาน:
 * - เส้น MACD ตัดเส้นสัญญาณจากล่างขึ้นบน = สัญญาณซื้อ (bullish)
 * - เส้น MACD ตัดเส้นสัญญาณจากบนลงล่าง = สัญญาณขาย (bearish)
 * - Histogram เปลี่ยนจากลบเป็นบวก = แนวโน้มเปลี่ยนเป็นขาขึ้น
 * - Histogram เปลี่ยนจากบวกเป็นลบ = แนวโน้มเปลี่ยนเป็นขาลง
 * - การแยกตัวของ MACD และราคา (Divergence) = สัญญาณการกลับตัวที่แข็งแกร่ง
 * 
 * 
 * 3. การใช้ EMA (Exponential Moving Average)
 * -------------------------------------
 * ในแอปมี EMA9 (สั้น) และ EMA21 (กลาง)
 * 
 * วิธีการอ่านค่า:
 * - ราคาอยู่เหนือ EMA = แนวโน้มขาขึ้น
 * - ราคาอยู่ใต้ EMA = แนวโน้มขาลง
 * - EMA9 อยู่เหนือ EMA21 = แนวโน้มขาขึ้นระยะสั้น
 * - EMA9 อยู่ใต้ EMA21 = แนวโน้มขาลงระยะสั้น
 * 
 * เทคนิคการใช้งาน:
 * - EMA9 ตัด EMA21 จากล่างขึ้นบน = สัญญาณซื้อ (Golden Cross ระยะสั้น)
 * - EMA9 ตัด EMA21 จากบนลงล่าง = สัญญาณขาย (Death Cross ระยะสั้น)
 * - ใช้ EMA เป็นแนวรับ/แนวต้าน: หากราคาถอยมาแตะ EMA แล้วเด้งขึ้น = แนวรับที่แข็งแกร่ง
 * 
 * 
 * 4. การใช้ SMA (Simple Moving Average)
 * --------------------------------
 * ในแอปมี SMA50 (กลาง-ยาว) และ SMA200 (ยาว)
 * 
 * วิธีการอ่านค่า:
 * - ราคาอยู่เหนือ SMA50 และ SMA200 = แนวโน้มขาขึ้นที่แข็งแกร่ง
 * - ราคาอยู่ระหว่าง SMA50 และ SMA200 = แนวโน้มไม่ชัดเจน/แกว่งตัว
 * - ราคาอยู่ต่ำกว่าทั้ง SMA50 และ SMA200 = แนวโน้มขาลงที่แข็งแกร่ง
 * 
 * เทคนิคการใช้งาน:
 * - SMA50 ตัด SMA200 จากล่างขึ้นบน = Golden Cross, สัญญาณซื้อระยะยาว
 * - SMA50 ตัด SMA200 จากบนลงล่าง = Death Cross, สัญญาณขายระยะยาว
 * - SMA200 มักถูกใช้เป็นแนวรับ/แนวต้านสำคัญในตลาดระยะยาว
 * 
 * 
 * 5. การใช้ตัวบ่งชี้หลายตัวร่วมกัน
 * -------------------------
 * การใช้ตัวบ่งชี้เพียงตัวเดียวอาจไม่เพียงพอสำหรับการตัดสินใจเทรด ลองผสมผสานเทคนิคต่างๆ ดังนี้:
 * 
 * แนวทางที่ 1: ยืนยันแนวโน้ม
 * - ตรวจสอบแนวโน้มหลักด้วย SMA200
 * - ตรวจสอบแนวโน้มระยะกลางด้วย SMA50 หรือ EMA21
 * - ใช้ RSI หรือ MACD เพื่อหาจังหวะเข้าซื้อ/ขายที่ดีภายในแนวโน้มนั้น
 * 
 * แนวทางที่ 2: การยืนยันสัญญาณ
 * - เมื่อเห็นสัญญาณซื้อ/ขายจาก MACD, ตรวจสอบว่า RSI สนับสนุนสัญญาณนั้นหรือไม่
 * - ดูว่าราคาอยู่ในตำแหน่งใดเมื่อเทียบกับ EMA และ SMA
 * 
 * แนวทางที่ 3: ผสมผสานระยะเวลาเทรด
 * - สำหรับเทรดระยะสั้น: ใช้ EMA และ RSI เป็นหลัก
 * - สำหรับเทรดระยะกลาง: ใช้ SMA50 และ MACD
 * - สำหรับลงทุนระยะยาว: ใช้ SMA200 เป็นหลัก
 * 
 * คำแนะนำสำคัญ: ไม่มีตัวบ่งชี้ใดที่ถูกต้อง 100% ควรพิจารณาปัจจัยพื้นฐาน
 * สภาพตลาดโดยรวม และการบริหารความเสี่ยงร่วมด้วยเสมอ
 */

